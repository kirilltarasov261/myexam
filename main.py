current_list = st.session_state.db[subj]

if menu == "📊 Статистика":
    st.title(f"Ваш путь: {subj}")
    done = sum(1 for t in current_list if t["done"])
    st.metric("Пройдено", f"{done} / {len(current_list)}", f"{int(done/len(current_list)*100)}%")
    st.progress(done/len(current_list))
    st.markdown("---")
    cols = st.columns(3)
    for i, t in enumerate(current_list):
        cols[i % 3].write(f"{'✅' if t['done'] else '⬜'} {t['name']}")

elif menu == "📖 База Знаний":
    st.title("📚 Конспекты")
    for t in current_list:
        with st.container():
            st.markdown(f'<div class="topic-card"><h3>{t["name"]}</h3><p>{t["theory"]}</p></div>', unsafe_allow_html=True)
            c1, c2, c3 = st.columns(3)
            if c1.button("✅ Выучено" if not t["done"] else "🌟 Повторить", key=f"b_{t['id']}"):
                t["done"] = not t["done"]; save_data(); st.rerun()
            c2.link_button("📽 Видео", t["video"])
            label = "📝 Решу ОГЭ" if "ОГЭ" in subj else "📝 Решу ЕГЭ"
            c3.link_button(label, t["link"])

elif menu == "🧠 Тренажёр":
    st.title("⚡ Экспресс-контроль")
    choice = st.selectbox("Тема:", [t['name'] for t in current_list])
    target = next(t for t in current_list if t['name'] == choice)
    
    q_key = f"step_{target['id']}"
    if q_key not in st.session_state: st.session_state[q_key] = 0
    step = st.session_state[q_key]
    
    if step < 5:
        q = target['questions'][step]
        st.info(f"Вопрос {step+1}/5")
        st.write(f"### {q['q']}")
        ans = st.text_input("Ваш ответ:", key=f"i_{target['id']}_{step}")
        if st.button("Проверить"):
            if ans.strip().lower() == q['a'].lower():
                st.session_state[q_key] += 1
                st.success("Верно!"); st.rerun()
            else: st.error("Попробуйте еще раз!")
    else:
        st.balloons(); st.success("Зачёт сдан!"); target["done"] = True; save_data()
        if st.button("Пройти заново"): st.session_state[q_key] = 0; st.rerun()

st.sidebar.markdown("---")
if st.sidebar.button("🗑 Сброс данных"):
    if os.path.exists(DB_FILE): os.remove(DB_FILE)
    st.rerun()
